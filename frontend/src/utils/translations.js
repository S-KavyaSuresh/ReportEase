function compileTemplates(dict) {
  return {
    ...dict,
    continueBtn: typeof dict.continueBtn === 'function'
      ? dict.continueBtn
      : (n) => (dict.continueBtn || T.English.continueBtn(n)).replace('{n}', n),
    speedLabel: typeof dict.speedLabel === 'function'
      ? dict.speedLabel
      : (r) => (dict.speedLabel || T.English.speedLabel(r)).replace('{r}', r),
    askSubtitle: typeof dict.askSubtitle === 'function'
      ? dict.askSubtitle
      : (lang) => (dict.askSubtitle || T.English.askSubtitle(lang)).replace('{lang}', lang),
  };
}

export function setRuntimeTranslations(language, values) {
  runtimeTranslations[language] = values || {};
}

export function getSerializableEnglish() {
  const base = T.English;
  return {
    ...base,
    continueBtn: 'Continue in {n} →',
    speedLabel: 'Speed: {r}×',
    askSubtitle: '● AI Assistant · Responds in {lang}',
  };
}

export function getT(language) {
  return compileTemplates({ ...T['English'], ...(T[language] || {}), ...(runtimeTranslations[language] || {}) });
}
